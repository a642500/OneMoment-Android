package co.yishun.onemoment.app.function;

/**
 * Represents an operation that accepts no input argument and returns no
 * result.
 * <p>
 * <p>This is a functional interface</a>
 * whose functional method is {@link #call()}.
 */
public interface Callback {

    /**
     * Performs an operation when called.
     */
    void call();


}